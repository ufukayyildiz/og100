import React from "react";

interface PreviewImageProps {
  src: string;
}

const PreviewImage: React.FC<PreviewImageProps> = ({ src }) => {
  const [loading, setLoading] = React.useState(false);
  const [error, setError] = React.useState(false);

  React.useEffect(() => {
    setError(false);

    if (!src) {
      return;
    }
    setLoading(true);
  }, [src]);

  const renderImageAndLoading = () => {
    return (
      <>
        <img
          src={src}
          alt="Open Graph Image Generated by DOGimg"
          className="object-cover h-auto w-full"
          style={{ display: !loading ? "block" : "none" }}
          onLoad={() => setLoading(false)}
          onError={(e) => {
            setLoading(false);
            setError(true);
          }}
        />
        {loading && (
          <div className="flex h-16 items-center justify-center">
            <div className="spinner border-t-2 border-gray-500"></div>
          </div>
        )}
      </>
    );
  };

  const renderNoImage = () => {
    return (
      <div className="flex h-16 items-center justify-center">
        <div className="text-gray-500 text-xl">
          Enter a URL to generate an open graph image
        </div>
      </div>
    );
  };

  const renderError = () => {
    return (
      <div className="flex h-16 items-center justify-center">
        <div className="text-red-500 text-xl">Error Loading Image</div>
      </div>
    );
  };

  return (
    <div className="w-full">
      {error ? renderError() : src ? renderImageAndLoading() : renderNoImage()}
    </div>
  );
};

export default PreviewImage;
